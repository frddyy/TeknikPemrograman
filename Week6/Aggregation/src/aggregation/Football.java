public class Football {
    //Attributes
    private String club;
    private String league;
    
    //Constructor
    public Football(String club, String league) {
        this.club = club;
        this.league = league;
    }
    //Getter
    public String getClub() {
        return club;
    }
    public String getLeague() {
        return league;
    }    
    
}
